var endTime = false;
function updateTime(){
  var date = new Date();
  var hour = date.getHours();
  var minute = date.getMinutes();
  if(hour < 10){ hour = "0" + hour; }
  if(minute < 10){ minute = "0" + minute; }  
  document.getElementById("time").innerHTML = hour + ":" + minute; 
  if(!endTime){  
    requestAnimationFrame(updateTime);  
  }
}

